package com.revature.models;

public class ChallengeEmployee {
	private int e_id;
	private String e_fname;
	private String e_lname;
	private String e_role;
	private int e_age;
	
	
public ChallengeEmployee() {
		super();
	}
public ChallengeEmployee(String e_fname, String e_lname, String e_role, int e_age) {
		super();
		this.e_fname = e_fname;
		this.e_lname = e_lname;
		this.e_role = e_role;
		this.e_age = e_age;
	}
public ChallengeEmployee(int e_id, String e_fname, String e_lname, String e_role, int e_age) {
		super();
		this.e_id = e_id;
		this.e_fname = e_fname;
		this.e_lname = e_lname;
		this.e_role = e_role;
		this.e_age = e_age;
	}
//	GETTERS & SETTERS
	public int getE_id() {
		return e_id;
	}
	public void setE_id(int e_id) {
		this.e_id = e_id;
	}
	public String getE_fname() {
		return e_fname;
	}
	public void setE_fname(String e_fname) {
		this.e_fname = e_fname;
	}
	public String getE_lname() {
		return e_lname;
	}
	public void setE_lname(String e_lname) {
		this.e_lname = e_lname;
	}
	public String getE_role() {
		return e_role;
	}
	public void setE_role(String e_role) {
		this.e_role = e_role;
	}
	public int getE_age() {
		return e_age;
	}
	public void setE_age(int e_age) {
		this.e_age = e_age;
	}
}
